package cn.com.beans.liu;

public class WarehouseBeans {
	private String Warehouse_id;
	private String Warehouse_name;
	private String Warehouse_head;
	private String Warehouse_tel;
	private String Warehouse_addr;
	private String Warehouse_note;
	public String getWarehouse_id() {
		return Warehouse_id;
	}
	public void setWarehouse_id(String warehouse_id) {
		Warehouse_id = warehouse_id;
	}
	public String getWarehouse_name() {
		return Warehouse_name;
	}
	public void setWarehouse_name(String warehouse_name) {
		Warehouse_name = warehouse_name;
	}
	public String getWarehouse_head() {
		return Warehouse_head;
	}
	public void setWarehouse_head(String warehouse_head) {
		Warehouse_head = warehouse_head;
	}
	public String getWarehouse_tel() {
		return Warehouse_tel;
	}
	public void setWarehouse_tel(String warehouse_tel) {
		Warehouse_tel = warehouse_tel;
	}
	public String getWarehouse_addr() {
		return Warehouse_addr;
	}
	public void setWarehouse_addr(String warehouse_addr) {
		Warehouse_addr = warehouse_addr;
	}
	public String getWarehouse_note() {
		return Warehouse_note;
	}
	public void setWarehouse_note(String warehouse_note) {
		Warehouse_note = warehouse_note;
	}
	
	public String toString() {
		return Warehouse_name;
		
	}
	
}
