package cn.com.beans.liu;

public class OperatorBeans {
	private String Operator_id;
	private String Operator_name;
	private String Operator_pwd;
	private String Operator_title;
	public String getOperator_id() {
		return Operator_id;
	}
	public void setOperator_id(String operator_id) {
		Operator_id = operator_id;
	}
	public String getOperator_name() {
		return Operator_name;
	}
	public void setOperator_name(String operator_name) {
		Operator_name = operator_name;
	}
	public String getOperator_pwd() {
		return Operator_pwd;
	}
	public void setOperator_pwd(String operator_pwd) {
		Operator_pwd = operator_pwd;
	}
	public String getOperator_title() {
		return Operator_title;
	}
	public void setOperator_title(String operator_title) {
		Operator_title = operator_title;
	}
	
}
