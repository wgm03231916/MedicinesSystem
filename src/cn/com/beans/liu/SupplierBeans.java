package cn.com.beans.liu;

public class SupplierBeans {
	private String Supplier_id;
	private String Supplier_name;
	private String Supplier_contact;
	private String Supplier_tel;
	private String Supplier_addr;
	private String Supplier_note;
	public String getSupplier_id() {
		return Supplier_id;
	}
	public void setSupplier_id(String supplier_id) {
		Supplier_id = supplier_id;
	}
	public String getSupplier_name() {
		return Supplier_name;
	}
	public void setSupplier_name(String supplier_name) {
		Supplier_name = supplier_name;
	}
	public String getSupplier_contact() {
		return Supplier_contact;
	}
	public void setSupplier_contact(String supplier_contact) {
		Supplier_contact = supplier_contact;
	}
	public String getSupplier_tel() {
		return Supplier_tel;
	}
	public void setSupplier_tel(String supplier_tel) {
		Supplier_tel = supplier_tel;
	}
	public String getSupplier_addr() {
		return Supplier_addr;
	}
	public void setSupplier_addr(String supplier_addr) {
		Supplier_addr = supplier_addr;
	}
	public String getSupplier_note() {
		return Supplier_note;
	}
	public void setSupplier_note(String supplier_note) {
		Supplier_note = supplier_note;
	}

}
